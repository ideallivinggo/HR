package com.aaa.rzhr.pojo;


public class Performance {

  private Integer perid;
  private String pername;
  private String perstartdate;
  private String peroverdate;
  private Integer tanid;
  private Integer deptid;
  private String perappraiser;


  public Integer getPerid() {
    return perid;
  }

  public void setPerid(Integer perid) {
    this.perid = perid;
  }


  public String getPername() {
    return pername;
  }

  public void setPername(String pername) {
    this.pername = pername;
  }


  public String getPerstartdate() {
    return perstartdate;
  }

  public void setPerstartdate(String perstartdate) {
    this.perstartdate = perstartdate;
  }


  public String getPeroverdate() {
    return peroverdate;
  }

  public void setPeroverdate(String peroverdate) {
    this.peroverdate = peroverdate;
  }


  public Integer getTanid() {
    return tanid;
  }

  public void setTanid(Integer tanid) {
    this.tanid = tanid;
  }


  public Integer getDeptid() {
    return deptid;
  }

  public void setDeptid(Integer deptid) {
    this.deptid = deptid;
  }


  public String getPerappraiser() {
    return perappraiser;
  }

  public void setPerappraiser(String perappraiser) {
    this.perappraiser = perappraiser;
  }

}
